from PIL import Image
import os

# 16x16 픽셀 머핀 이미지 생성
def create_muffin_texture():
    # 이미지 생성 (16x16, RGBA)
    img = Image.new('RGBA', (16, 16), (0, 0, 0, 0))
    pixels = img.load()
    
    # 머핀 모양 그리기
    # 갈색 베이스 (139, 69, 19, 255)
    brown = (139, 69, 19, 255)
    # 크림색 토핑 (255, 248, 220, 255)
    cream = (255, 248, 220, 255)
    # 더 진한 갈색 (101, 67, 33, 255)
    dark_brown = (101, 67, 33, 255)
    
    # 머핀 베이스 그리기
    muffin_pattern = [
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0001111011110000",
        "0011111101110000",
        "0011111111110000",
        "0111111111111000",
        "0111111111111000",
        "0111111111111000",
        "0111111111111000",
        "0111111111111000",
        "0111111111111000",
        "0111111111111000",
        "0011111111110000",
        "0001111111100000",
        "0000000000000000"
    ]
    
    # 토핑 패턴
    topping_pattern = [
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0002222022220000",
        "0022222202220000",
        "0022222222220000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000",
        "0000000000000000"
    ]
    
    # 베이스 색칠
    for y in range(16):
        for x in range(16):
            if muffin_pattern[y][x] == '1':
                pixels[x, y] = brown
            elif muffin_pattern[y][x] == '2':
                pixels[x, y] = dark_brown
    
    # 토핑 색칠
    for y in range(16):
        for x in range(16):
            if topping_pattern[y][x] == '2':
                pixels[x, y] = cream
    
    return img

# 현재 스크립트 위치에 이미지 저장
if __name__ == "__main__":
    muffin_img = create_muffin_texture()
    muffin_img.save("muffin.png")
    print("muffin.png 파일이 생성되었습니다!")
    print("크기: 16x16 픽셀")
    print("형식: PNG (투명 배경 지원)")
